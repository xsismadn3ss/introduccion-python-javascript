## Una variable puede cambiar el valor si uno lo desea

a = 0 # variable
print(a)


# Una constante no puede cambiar de valor una vez se define
b = ("hola", "mundo") # constante

a += 1
print(a)

# b[0] = "adios"

b = ("adios",)

print(b)

